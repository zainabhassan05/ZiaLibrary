package com.strings;

public class EvenAndOdd {

	public static void main(String[] args) {
		//find odd number from 1 to 50
		
		for(int i=0; i<=50;i++) {
			if(i%2 !=0) {
				System.out.println("This is Odd Number = "+i);
			} else {
				System.out.println("This is Even Number = "+i);
			}
				
		}		

	}

}
